using System;
using System.Data;
using System.Data.Common;
using CRM.Model.AdministrationModel;
using Microsoft.Practices.EnterpriseLibrary.Data;
using Microsoft.Practices.EnterpriseLibrary.ExceptionHandling;
using System.Collections;
using System.Data.SqlClient;

namespace CRM.DataAccess.AdministratorEntity
{
    public class NewsStoredProcedure
    {
        public void InsertUpdateNews(ArrayList News)
        {
            Database db = null;
            DbCommand dbCmd = null;
            try
            {
                db = DatabaseFactory.CreateDatabase(DALHelper.CRM_CONNECTION_STRING);
                dbCmd = db.GetStoredProcCommand("INSERT_UPDATE_FOR_NEWS_MASTER");
                db.AddInParameter(dbCmd, "@NEWS_ID", DbType.Int32, News[0]);
                db.AddInParameter(dbCmd, "@NEWS_HEADER", DbType.String, News[1]);
                db.AddInParameter(dbCmd, "@NEWS_DESCRIPTION", DbType.String, News[2]);
                db.AddInParameter(dbCmd, "@DISPLAY", DbType.String, News[3]);
                //db.AddInParameter(dbCmd, "@USER", DbType.Int32, Convert.ToInt32(Group[2]));
                db.ExecuteNonQuery(dbCmd);
            }
            catch (Exception ex)
            {
                bool rethrow = ExceptionPolicy.HandleException(ex, DALHelper.DAL_EXP_POLICYNAME);
                if (rethrow)
                {
                    throw ex;

                }
            }
            finally
            {
                DALHelper.Destroy(ref dbCmd);
            }
        }
        public void delNews(int newsid)
        {
            Database db = null;
            DbCommand dbCmd = null;
            try
            {
                db = DatabaseFactory.CreateDatabase(DALHelper.CRM_CONNECTION_STRING);
                dbCmd = db.GetStoredProcCommand("DELETE_FROM_NEWS_MASTER");
                db.AddInParameter(dbCmd, "@NEWS_ID", DbType.Int32, newsid);
                db.ExecuteNonQuery(dbCmd);
            }
            catch (Exception ex)
            {
                bool rethrow = ExceptionPolicy.HandleException(ex, DALHelper.DAL_EXP_POLICYNAME);
                if (rethrow)
                {
                    throw ex;
                }
            }
            finally
            {
                DALHelper.Destroy(ref dbCmd);
            }
        }
    }
}
